import test from 'node:test';
import assert from 'node:assert/strict';
import fs from 'node:fs';
import path from 'node:path';
import url from 'node:url';

function readRepoFile(relPath) {
  const here = path.dirname(url.fileURLToPath(import.meta.url));
  const repoRoot = path.resolve(here, '..', '..', '..');
  return fs.readFileSync(path.join(repoRoot, relPath), 'utf8');
}

test('expo native local builds must not symlink repo node_modules into a staged build context (fingerprint runtimeVersion drift)', () => {
  const src = readRepoFile('scripts/pipeline/expo/native-build.mjs');
  assert.ok(
    !src.includes('maybeLinkNodeModulesIntoStage({ repoRoot, stagedRepoDir: effectiveRepoDir, dryRun })'),
    'native-build.mjs should not symlink node_modules into staged repo for local builds (breaks fingerprint runtimeVersion)',
  );
});


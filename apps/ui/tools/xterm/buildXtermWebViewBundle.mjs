import fs from 'node:fs';
import path from 'node:path';
import process from 'node:process';
import url from 'node:url';
import { createRequire } from 'node:module';
import esbuild from 'esbuild';

const require = createRequire(import.meta.url);

function writeIfChanged(filePath, contents) {
    const existing = fs.existsSync(filePath) ? fs.readFileSync(filePath, 'utf8') : null;
    if (existing === contents) return false;
    fs.mkdirSync(path.dirname(filePath), { recursive: true });
    fs.writeFileSync(filePath, contents, 'utf8');
    return true;
}

function chunkString(value, chunkSize) {
    const out = [];
    for (let i = 0; i < value.length; i += chunkSize) {
        out.push(value.slice(i, i + chunkSize));
    }
    return out;
}

function findRepoRoot(startDir) {
    let dir = startDir;
    for (let i = 0; i < 8; i++) {
        if (fs.existsSync(path.resolve(dir, 'package.json')) && fs.existsSync(path.resolve(dir, 'yarn.lock'))) {
            return dir;
        }
        const parent = path.dirname(dir);
        if (parent === dir) break;
        dir = parent;
    }
    return path.resolve(startDir, '..');
}

function createEntrySource() {
    // Note: this bundle is executed inside a WebView. Keep it browser-compatible and avoid Node globals.
    return `
import { Terminal } from '@xterm/xterm';
import { FitAddon } from '@xterm/addon-fit';
import { WebLinksAddon } from '@xterm/addon-web-links';

globalThis.HAPPIER_XTERM_WEBVIEW = {
  Terminal,
  FitAddon,
  WebLinksAddon,
};
`;
}

function readXtermCss({ expoAppDir, repoRootDir }) {
    const candidatePaths = [
        path.resolve(expoAppDir, 'node_modules', '@xterm', 'xterm', 'css', 'xterm.css'),
        path.resolve(repoRootDir, 'node_modules', '@xterm', 'xterm', 'css', 'xterm.css'),
    ];

    const resolvedCandidate = candidatePaths.find((p) => fs.existsSync(p));
    if (resolvedCandidate) {
        return { css: fs.readFileSync(resolvedCandidate, 'utf8'), cssPath: resolvedCandidate };
    }

    try {
        const resolved = require.resolve('@xterm/xterm/css/xterm.css');
        if (fs.existsSync(resolved)) {
            return { css: fs.readFileSync(resolved, 'utf8'), cssPath: resolved };
        }
    } catch {
        // ignore
    }

    return { css: '', cssPath: null };
}

export async function buildXtermWebViewBundle({ expoAppDir, repoRootDir }) {
    const entry = createEntrySource();

    const result = await esbuild.build({
        absWorkingDir: expoAppDir,
        bundle: true,
        write: false,
        format: 'iife',
        platform: 'browser',
        target: ['es2020'],
        minify: true,
        legalComments: 'eof',
        sourcemap: false,
        treeShaking: true,
        stdin: {
            contents: entry,
            loader: 'ts',
            resolveDir: expoAppDir,
            sourcefile: 'xterm-webview-entry.ts',
        },
    });

    const js = result.outputFiles?.[0]?.text ?? '';
    if (!js.trim()) {
        throw new Error('esbuild produced an empty Xterm bundle');
    }

    const { css } = readXtermCss({ expoAppDir, repoRootDir });

    const jsChunks = chunkString(js, 50_000);
    const ts = `/**
 * Generated by \`apps/ui/tools/xterm/buildXtermWebViewBundle.mjs\`.
 *
 * This file exists in source control as a safe fallback so the app can still run.
 * Postinstall replaces it with an embedded, offline-friendly bundle for native
 * Xterm WebView terminals.
 */
const XTERM_WEBVIEW_BUNDLE_CHUNKS = [
${jsChunks.map((chunk) => `    ${JSON.stringify(chunk)},`).join('\n')}
];

export const XTERM_WEBVIEW_BUNDLE_JS = XTERM_WEBVIEW_BUNDLE_CHUNKS.join('');

export const XTERM_WEBVIEW_CSS = ${JSON.stringify(css)};
`;

    const outFile = path.resolve(
        expoAppDir,
        'sources',
        'components',
        'terminal',
        'xterm',
        'webview',
        'xtermWebViewAssets.generated.ts',
    );

    const wrote = writeIfChanged(outFile, ts);
    return { wrote, outFile, bundleBytes: Buffer.byteLength(js, 'utf8'), cssBytes: Buffer.byteLength(css, 'utf8') };
}

if (import.meta.url === url.pathToFileURL(process.argv[1]).href) {
    const toolsDir = path.dirname(fs.realpathSync(url.fileURLToPath(import.meta.url)));
    const expoAppDir = path.resolve(toolsDir, '..', '..');
    const repoRootDir = findRepoRoot(expoAppDir);

    buildXtermWebViewBundle({ expoAppDir, repoRootDir })
        .then((result) => {
            // eslint-disable-next-line no-console
            console.log(
                `[xterm] webview bundle ${result.wrote ? 'written' : 'up to date'}: ${path.relative(
                    expoAppDir,
                    result.outFile,
                )} (${Math.round(result.bundleBytes / 1024)}kb js, ${Math.round(result.cssBytes / 1024)}kb css)`,
            );
        })
        .catch((err) => {
            // eslint-disable-next-line no-console
            console.error(err instanceof Error ? err.message : String(err));
            process.exit(1);
        });
}


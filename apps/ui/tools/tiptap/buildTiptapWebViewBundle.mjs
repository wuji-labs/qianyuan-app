import fs from 'node:fs';
import path from 'node:path';
import process from 'node:process';
import url from 'node:url';
import { createRequire } from 'node:module';
import esbuild from 'esbuild';

const require = createRequire(import.meta.url);

function writeIfChanged(filePath, contents) {
    const existing = fs.existsSync(filePath) ? fs.readFileSync(filePath, 'utf8') : null;
    if (existing === contents) return false;
    fs.mkdirSync(path.dirname(filePath), { recursive: true });
    fs.writeFileSync(filePath, contents, 'utf8');
    return true;
}

function chunkString(value, chunkSize) {
    const out = [];
    for (let i = 0; i < value.length; i += chunkSize) {
        out.push(value.slice(i, i + chunkSize));
    }
    return out;
}

function findRepoRoot(startDir) {
    let dir = startDir;
    for (let i = 0; i < 8; i++) {
        if (fs.existsSync(path.resolve(dir, 'package.json')) && fs.existsSync(path.resolve(dir, 'yarn.lock'))) {
            return dir;
        }
        const parent = path.dirname(dir);
        if (parent === dir) break;
        dir = parent;
    }
    return path.resolve(startDir, '..');
}

// The esbuild entry: bundle the headless TipTap WebView entry, which assigns
// `globalThis.HAPPIER_TIPTAP_WEBVIEW`. No React (D4).
const ENTRY_RELATIVE_PATH = path.join(
    'sources',
    'components',
    'ui',
    'markdown',
    'editor',
    'bridge',
    'tiptapWebViewEntry.ts',
);

const OUTPUT_RELATIVE_PATH = path.join(
    'sources',
    'components',
    'ui',
    'markdown',
    'editor',
    'bridge',
    'tiptapWebViewBundle.generated.ts',
);

/**
 * Reads + concatenates the ProseMirror base CSS needed for a usable
 * contenteditable surface (caret, gapcursor). TipTap ships no CSS of its own;
 * these are the canonical ProseMirror stylesheets.
 */
function readProseMirrorCss({ expoAppDir, repoRootDir }) {
    const cssModules = [
        'prosemirror-view/style/prosemirror.css',
        'prosemirror-gapcursor/style/gapcursor.css',
    ];

    let css = '';
    for (const moduleId of cssModules) {
        const candidatePaths = [
            path.resolve(expoAppDir, 'node_modules', moduleId),
            path.resolve(repoRootDir, 'node_modules', moduleId),
        ];
        let resolved = candidatePaths.find((p) => fs.existsSync(p));
        if (!resolved) {
            try {
                const r = require.resolve(moduleId);
                if (fs.existsSync(r)) resolved = r;
            } catch {
                // ignore
            }
        }
        if (resolved) {
            css += `\n/* ${moduleId} */\n${fs.readFileSync(resolved, 'utf8')}`;
        }
    }
    return css.trim();
}

function applyThemeVariablesToProseMirrorCss(css) {
    return css
        .replace(
            /outline:\s*2px solid #8cf;/g,
            'outline: 2px solid var(--happier-tiptap-selection-accent-color);',
        )
        .replace(
            /border:\s*2px solid #8cf;/g,
            'border: 2px solid var(--happier-tiptap-selection-accent-color);',
        )
        .replace(
            /border-top:\s*1px solid black;/g,
            'border-top: 1px solid var(--happier-tiptap-gap-cursor-color);',
        );
}

export async function buildTiptapWebViewBundle({ expoAppDir, repoRootDir }) {
    const entryPath = path.resolve(expoAppDir, ENTRY_RELATIVE_PATH);
    if (!fs.existsSync(entryPath)) {
        throw new Error(`TipTap WebView entry not found at ${entryPath}`);
    }

    const result = await esbuild.build({
        absWorkingDir: expoAppDir,
        entryPoints: [entryPath],
        bundle: true,
        write: false,
        format: 'iife',
        platform: 'browser',
        target: ['es2020'],
        minify: true,
        legalComments: 'eof',
        sourcemap: false,
        treeShaking: true,
    });

    const js = result.outputFiles?.[0]?.text ?? '';
    // D9: there is NO CDN fallback. An empty bundle is fatal at build time so a
    // broken build can never ship an unusable native editor.
    if (!js.trim()) {
        throw new Error('esbuild produced an empty TipTap WebView bundle');
    }

    const css = applyThemeVariablesToProseMirrorCss(readProseMirrorCss({ expoAppDir, repoRootDir }));

    const jsChunks = chunkString(js, 50_000);
    const ts = `/**
 * Generated by \`apps/ui/tools/tiptap/buildTiptapWebViewBundle.mjs\`.
 *
 * This file embeds the headless TipTap WebView runtime (assigns
 * \`globalThis.HAPPIER_TIPTAP_WEBVIEW\`) + ProseMirror CSS for the native rich
 * markdown editor. Unlike CodeMirror/Xterm there is NO CDN fallback (D9): when
 * empty the native surface fails closed to raw mode.
 */
const TIPTAP_WEBVIEW_BUNDLE_CHUNKS = [
${jsChunks.map((chunk) => `    ${JSON.stringify(chunk)},`).join('\n')}
];

export const TIPTAP_WEBVIEW_BUNDLE_JS = TIPTAP_WEBVIEW_BUNDLE_CHUNKS.join('');

export const TIPTAP_WEBVIEW_CSS = ${JSON.stringify(css)};
`;

    const outFile = path.resolve(expoAppDir, OUTPUT_RELATIVE_PATH);
    const wrote = writeIfChanged(outFile, ts);
    return { wrote, outFile, bundleBytes: Buffer.byteLength(js, 'utf8'), cssBytes: Buffer.byteLength(css, 'utf8') };
}

if (import.meta.url === url.pathToFileURL(process.argv[1]).href) {
    const toolsDir = path.dirname(fs.realpathSync(url.fileURLToPath(import.meta.url)));
    const expoAppDir = path.resolve(toolsDir, '..', '..');
    const repoRootDir = findRepoRoot(expoAppDir);

    buildTiptapWebViewBundle({ expoAppDir, repoRootDir })
        .then((result) => {
            // eslint-disable-next-line no-console
            console.log(
                `[tiptap] webview bundle ${result.wrote ? 'written' : 'up to date'}: ${path.relative(
                    expoAppDir,
                    result.outFile,
                )} (${Math.round(result.bundleBytes / 1024)}kb js, ${Math.round(result.cssBytes / 1024)}kb css)`,
            );
        })
        .catch((err) => {
            // eslint-disable-next-line no-console
            console.error(err instanceof Error ? err.message : String(err));
            process.exit(1);
        });
}
